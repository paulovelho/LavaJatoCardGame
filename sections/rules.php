                <p>
                    São tempos interessantes na fictícia Ilha de Vera Cruz.
                </p>
                <p>
                    O fictício governo está acuado, acusado de diversos casos de corrupção. A fictícia presidenta perde aliados políticos com a mesma freqüência que sua população perde empregos.
                </p>
                <p>
                    Um fictício juiz comanda uma operação de limpeza política cujo principal objetivo é tirar a presidenta do poder. Ambos precisam de aliados, mesmo que isso inclua se envolver com políticos criminosos...
                </p>
                <h4>O jogo</h4>
                <p>
                    Neste jogo de cartas, você pode acompanhar os fictícios processos da tentativa de impeachment tramada pela oposição. Os jogadores tomam aleatoriamente os papéis do juiz, da presidenta e de diversos políticos (todos fictícios) que podem ser contra ou a favor do atual fictício governo. Cada um joga em sua vez e pode executar ações de forma a cumprir o objetivo que pode ser de manter a presidenta ou de conseguir o impeachment. A ação de todos é relevante para o resultado final.
                </p>
                <p>
                    Operação Lava Jato é diversão pra toda família. Com riscos de textão de facebook e fim de suas amizades.
                </p>
                <br/>
                <h4>Detalhes</h4>
                <ul>
                    <li>Jogadores: de 6 a 10 jogadores</li>
                    <li>Idade mínima recomendada: 16 anos</li>
                    <li>Tempo de jogo: entre 30 e 45 minutos</li>
                </ul><br/>
                <h4>Como jogar</h4>
                <h5>Setup de jogo:</h5>
                <p>
                    Separar as cartas da presidenta e do juiz.
                </p>
                <p>
                    Dentre as cartas de personagens, separar um número igual dentre políticos coxinhas com +1 e políticos petralhas com -1 até que se complete o número de jogadores. Em caso de número ímpar, deve haver mais petralhas.
                </p>
                <p>
                    Os jogadores sentam-se em círculo. As cartas são embaralhadas e distribuídas. Os jogadores que saírem com as cartas da presidenta e do juiz identificam-se e usam suas placas de identificação. Os outros jogadores mantém suas identidades secretas. <b>Todos os políticos pegam um crime</b> do baralho de crimes. O crime de cada político inicialmente só será de conhecimento do mesmo. Cada político também pega uma carta de posicionamento, com dois lados: um deles como posição de coxinha e outro como posição de petralha.
                </p>
                <h5>Jogada da Presidenta</h5>
                <p>
                    A presidenta sempre começa jogando. No início de cada rodada ela pergunta quais jogadores políticos vão apoiá-la. Os jogadores então colocam suas cartas de posicionamento viradas com suas inclinações para aquela rodada, independente de seus reais objetivos. A presidenta então pega o deck de cartas ação, embaralha-o e distribui duas para cada político.
                </p>
                <p>
                    Na sua rodada, a presidenta pode executar uma das seguintes ações:
                </p>
                <ul>
                    <li><b>Mandar o Bessias:</b> Enviar o bessias com o papel para alguém de posicionamento vermelho. O político com o bessias não pode ser preso ou indiciado. Se o político estiver preso, não pode receber o bessias.</li>
                    <li><b>Imprensa vermelha:</b> Paralisa a ação do juiz na rodada.</li>
                    <li><b>Não vai ter golpe:</b> Libera um político preso. Japonês da Federal volta para as mãos do juiz.</li>
                    <li><b>Indiciar:</b> Pode indiciar um político - o político indiciado não pode votar pelo impeachment.</li>
                    <li><b>Eleger ministro:</b> Retirar três cartas do baralho de influências e escolher uma para ser ministro. Os votos do ministro contarão na votação de impeachment. <a href="#aprovacao-politica">Requer aprovação política</a> (após personagem ser escolhido).</li>
                    <li><b>Revelar posição:</b> A presidenta escolhe um político que deve mostrar o seu personagem e jogar com o posicionamento relativo à sua real identidade. <a href="#aprovacao-politica">Requer aprovação política</a> (após jogador ser escolhido).</li>
                    <li><b>Remover influência:</b> Escolhe uma influência ativa na mesa para remover, retornando-a ao baralho de personagens. <a href="#aprovacao-politica">Requer aprovação política</a> (após personagem ser escolhido).</li>
                </ul>
                <p>
                    Após a jogada da presidenta, cada jogador joga em sua vez, de acordo com a sua posição na mesa, seguindo o sentido horário.
                </p>
                <h5>Jogada de cada político</h5>
                <p>
                    Cada político pode usar uma de suas cartas-ação (<a href="#aprovacao-politica">ver ações políticas</a>).<br/>
                    Jogadores presos não jogam, passando sua vez. Jogadores indiciados jogam.<br/>
                    Após a jogada, as cartas de ação voltam ao baralho.
                </p>
                <h5>Jogada do juiz</h5>
                <p>
                    O juiz <b>só pode executar ações contra jogadores que se posicionarem a favor do governo</b> durante aquela rodada. Ele pode executar uma das seguintes ações:
                </p>
                <ul>
                    <li><b>Investigar:</b> Ele pode ver o crime e a real filiação de um político. O político escolhido entrega secretamente seu crime e sua carta de personagem para o jogador juiz, que examina-as e devolve, sem mostrar aos outros jogadores.</li>
                    <li><b>Japonês da PF:</b> Prende um político. Ele entrega a carta do japonês ao jogador que ele deseja prender. O político que estiver com a carta do japonês da Federal não pode executar ações. Não é possível prender o político com o bessias.</li>
                    <li><b>Indiciar:</b> Escolhe um político para indiciar. O político escolhido deve exibir seu crime e todos os políticos que possuírem o mesmo crime devem se apresentar e também serão indiciados, independente de seus posicionamentos políticos.</li>
                </ul>
                <a name="aprovacao-politica"></a> 
                <h5>Ações políticas</h5>
                <ul>
                    <li><b>Influência:</b> Elege personagem para posição de influência. Retira aleatoriamente três cartas de personagem e escolhe uma delas para colocar na mesa. Podem haver diversas influências agindo ao mesmo tempo.</li>
                    <li><b>Delação premiada:</b> Remove influência da mesa. Retira uma influência da mesa e devolve ela ao baralho de personagens. Após uma influência ser removida, o baralho de personagens é reembaralhado.</li>
                    <li><b>Grampo:</b> Retira o efeito do Bessias. Carta do Bessias volta para a presidenta.</li>
                    <li><b>Liminar:</b> Suspende a posse do ministro. Carta deve ser colocada em cima da carta do ministro e não é embaralhada no final da rodada. O ministro suspenso não tem a influência contabilizada.</li>
                    <li><b>Suspender liminar:</b> Remove a liminar de cima da carta do ministro, recolocando sua influência no jogo. Ambas as cartas voltam para o baralho de ações.</li>
                    <li><b>Herói controverso:</b> Prende o japonês da PF, liberando o político preso. Carta do japonês da PF volta para o juiz mas não pode ser usada na próxima rodada. Pode ser usada pelo político preso.</li>
                    <li><b>Habeas Corpus:</b> Libera o jogador preso. Carta do japonês da PF volta para o juiz e pode ser usada na próxima rodada. Pode ser usada pelo político preso.</li>
                    <li><b>Laranja:</b> Um amigo que responde pelo crime do político. Remove a indiciação do político que a usar, podendo liberar o jogador caso ele esteja preso. Político descarta o crime e pega um novo crime do baralho.</li>
                    <li><b>Pedir impeachment:</b> abre votação de impeachment contra a presidenta (<a href="#votacao-impeachment">ver votação do impeachment</a>)</li>
                </ul>
                <a name="aprovacao-politica"></a> 
                <h5>Aprovação Política</h5>
                <p>
                    Em caso de medidas que exijam aprovação política, os políticos que apresentarem posicionamento favorável ao governo deverão fazer uma votação secreta. Cada um deverá receber uma carta sim e uma carta não, selecionar uma para o voto que vai ser contado e colocar a outra no baralho de descarte. Os dois baralhos (de votos e de descarte) devem ser embaralhados para que ninguém saiba qual foi o voto de cada jogador. Medidas são aprovadas em caso de empate ou maioria favorável.
                </p>
                <a name="votacao-impeachment"></a> 
                <h5>Votação de impeachment</h5>
                <p>
                    Para as votações de impeachment, os políticos que não estiverem presos ou indiciados votam. Cada um deverá receber uma carta sim e uma carta não, selecionar uma para o voto que vai ser contado e colocar a outra no baralho de descarte. Os dois baralhos (de votos e de descarte) devem ser embaralhados para que ninguém saiba qual foi o voto de cada jogador. Junto aos votos dos jogadores, as pontuações das influências atuando na mesa são somadas.
                </p>
                <p class="text-muted quote">
                    <i><b>Exemplo:</b> Em um jogo com 8 jogadores, um político abre impeachment. Na atual situação da mesa há um político indiciado, um preso, além da presidenta e do juiz. Restam quatro políticos elegíveis para votação. Cada um deles vota e descarta uma carta de voto. Na mesa, há duas cartas de influência de valor +1, uma carta de valor -1 e um ministro de valor -2.</i><br/>
                    <i>Somam-se as influências: +1 +1 -1 -2 = -1</i><br/>
                    <i>As influências somam-se às cartas de voto. Supondo que os votos sejam 3x sim e 1x não, o valor de (-1) garante um voto extra ao “não”. Resultado: 3 votos sim, 2 votos não.</i><br/>
                    Maioria simples dá vitória ao impeachment. Empate mantém a presidenta.
                </p>
                <h5>Processo de impeachment</h5>
                <p>
                    <b>Em caso de impeachment aprovado, a presidenta perde seus poderes de presidenta e passa a jogar como um político comum</b>, usando inclusive as cartas de ação. No momento que ela perder sua primeira votação, os posicionamentos de cada político não podem mais ser mudados.<br/>
                    Um novo impeachment remove completamente a presidenta do jogo garantindo a vitória do juiz e dos políticos coxinhas. A presidenta não vota em seu próprio impeachment.
                </p>
                <h5>Fim de jogo</h5>
                <p>
                    O juiz e os políticos coxinhas ganham se a presidenta sofrer impeachment.<br/><br/>
                    A presidenta e os políticos petralhas ganham se a presidenta ainda se mantiver no poder após n rodadas.<br/>
                    Número de rodadas: 14 - <i>(quantidade de jogadores)</i><br/><br/>
                    Por fim há a carta “povo”.<br/>
                    Não importa quem ganhar, o povo perde.
                </p>
